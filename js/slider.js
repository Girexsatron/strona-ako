document.addEventListener('DOMContentLoaded', function() {
    // Pre≈Çadowanie obraz√≥w t≈Ça
    preloadSliderImages();
    
    // Inicjalizujemy g≈Ç√≥wny slider na stronie g≈Ç√≥wnej
    initHeroSlider();
    
    // Inicjalizujemy slider z opiniami
    initTestimonialsSlider();
});

// üåÑ PRE≈ÅADOWANIE OBRAZ√ìW SLIDERA
function preloadSliderImages() {
    const slides = document.querySelectorAll('.slide');
    
    if (slides.length > 0) {
        slides.forEach(slide => {
            const bgElement = slide.querySelector('.slide-bg');
            if (bgElement) {
                const bgStyle = window.getComputedStyle(bgElement);
                const bgImage = bgStyle.backgroundImage;
                
                // Wyodrƒôbniamy URL obrazu
                const imageUrl = bgImage.match(/url\(['"]?(.*?)['"]?\)/);
                
                if (imageUrl && imageUrl[1]) {
                    // Pre≈Çadujemy obraz
                    const img = new Image();
                    img.src = imageUrl[1];
                    console.log('Pre≈Çadowanie obrazu:', imageUrl[1]);
                }
            }
        });
    }
}

// üñºÔ∏è G≈Å√ìWNY SLIDER - POPRAWIONA WERSJA
function initHeroSlider() {
    const heroSlider = document.querySelector('.hero-slider');
    
    // Je≈õli slider nie istnieje na tej stronie, ko≈Ñczymy
    if (!heroSlider) return;
    
    // Znajd≈∫ wszystkie slajdy
    const slides = heroSlider.querySelectorAll('.slide');
    
    // Je≈õli mamy tylko jeden slajd, nie potrzebujemy slidera
    if (slides.length <= 1) {
        if (slides.length === 1) {
            slides[0].classList.add('active');
        }
        return;
    }
    
    // Dodajemy przyciski nawigacyjne (je≈õli jeszcze nie istniejƒÖ)
    let prevButton = heroSlider.querySelector('.slider-nav.prev');
    let nextButton = heroSlider.querySelector('.slider-nav.next');
    
    if (!prevButton) {
        prevButton = document.createElement('button');
        prevButton.className = 'slider-nav prev';
        prevButton.innerHTML = '<i class="fas fa-chevron-left"></i>';
        prevButton.setAttribute('aria-label', 'Poprzedni slajd');
        heroSlider.appendChild(prevButton);
    }
    
    if (!nextButton) {
        nextButton = document.createElement('button');
        nextButton.className = 'slider-nav next';
        nextButton.innerHTML = '<i class="fas fa-chevron-right"></i>';
        nextButton.setAttribute('aria-label', 'Nastƒôpny slajd');
        heroSlider.appendChild(nextButton);
    }
    
    // Dodajemy wska≈∫niki slajd√≥w (je≈õli jeszcze nie istniejƒÖ)
    let indicators = heroSlider.querySelector('.slider-indicators');
    
    if (!indicators) {
        indicators = document.createElement('div');
        indicators.className = 'slider-indicators';
        
        for (let i = 0; i < slides.length; i++) {
            const dot = document.createElement('span');
            dot.className = i === 0 ? 'indicator active' : 'indicator';
            dot.dataset.slide = i;
            dot.setAttribute('aria-label', `Slajd ${i + 1}`);
            indicators.appendChild(dot);
        }
        
        heroSlider.appendChild(indicators);
    }
    
    // Pobieramy wszystkie kropki
    const dots = indicators.querySelectorAll('.indicator');
    
    // Zmienne do kontroli slidera
    let currentSlide = 0;
    let isAnimating = false;
    let autoPlayTimer;
    let autoPlayDelay = 7000; // D≈Çu≈ºszy czas wy≈õwietlania slajdu (7 sekund)
    
    // Sprawdzamy, czy jaki≈õ slajd jest ju≈º aktywny
    const activeSlide = heroSlider.querySelector('.slide.active');
    if (activeSlide) {
        // Znajdujemy indeks aktywnego slajdu
        slides.forEach((slide, index) => {
            if (slide === activeSlide) {
                currentSlide = index;
            }
        });
    } else {
        // Aktywujemy pierwszy slajd, je≈õli ≈ºaden nie jest aktywny
        slides[0].classList.add('active');
    }
    
    // Aktualizujemy wska≈∫niki, aby odpowiada≈Çy aktualnemu slajdowi
    updateIndicators();
    
    // Funkcja pokazujƒÖca dany slajd
    function showSlide(index) {
        if (isAnimating) return;
        isAnimating = true;
        
        // Ukrywamy aktualny slajd
        slides[currentSlide].classList.remove('active');
        
        // Aktualizujemy indeks aktualnego slajdu
        currentSlide = index;
        
        // Zapewniamy, ≈ºe indeks jest w prawid≈Çowym zakresie
        if (currentSlide >= slides.length) currentSlide = 0;
        if (currentSlide < 0) currentSlide = slides.length - 1;
        
        // Pokazujemy nowy slajd
        slides[currentSlide].classList.add('active');
        
        // Aktualizujemy wska≈∫niki
        updateIndicators();
        
        // Po zako≈Ñczeniu animacji przej≈õcia zezwalamy na kolejne przej≈õcie
        setTimeout(() => {
            isAnimating = false;
        }, 1500); // Czas zgodny z transition w CSS (1.5s)
    }
    
    // Funkcja aktualizujƒÖca wska≈∫niki
    function updateIndicators() {
        dots.forEach((dot, i) => {
            if (i === currentSlide) {
                dot.classList.add('active');
            } else {
                dot.classList.remove('active');
            }
        });
    }
    
    // Funkcja prze≈ÇƒÖczajƒÖca do nastƒôpnego slajdu
    function nextSlide() {
        showSlide(currentSlide + 1);
    }
    
    // Funkcja prze≈ÇƒÖczajƒÖca do poprzedniego slajdu
    function prevSlide() {
        showSlide(currentSlide - 1);
    }
    
    // Ustawiamy automatyczne prze≈ÇƒÖczanie slajd√≥w
    function startAutoPlay() {
        // Czyszczenie istniejƒÖcego timera dla pewno≈õci
        clearInterval(autoPlayTimer);
        autoPlayTimer = setInterval(nextSlide, autoPlayDelay);
    }
    
    function stopAutoPlay() {
        clearInterval(autoPlayTimer);
    }
    
    // Obs≈Çuga przycisk√≥w nawigacyjnych
    prevButton.addEventListener('click', () => {
        stopAutoPlay();
        prevSlide();
        startAutoPlay();
    });
    
    nextButton.addEventListener('click', () => {
        stopAutoPlay();
        nextSlide();
        startAutoPlay();
    });
    
    // Obs≈Çuga klikniƒôcia wska≈∫nik√≥w
    dots.forEach(dot => {
        dot.addEventListener('click', () => {
            const slideIndex = parseInt(dot.dataset.slide);
            if (currentSlide !== slideIndex) { // Zapobiegamy zbƒôdnym przej≈õciom
                stopAutoPlay();
                showSlide(slideIndex);
                startAutoPlay();
            }
        });
    });
    
    // Dodajemy obs≈Çugƒô klawiszy do nawigacji
    heroSlider.setAttribute('tabindex', '0'); // Dodajemy mo≈ºliwo≈õƒá fokusowania
    
    heroSlider.addEventListener('keydown', (e) => {
        if (e.key === 'ArrowLeft') {
            stopAutoPlay();
            prevSlide();
            startAutoPlay();
        } else if (e.key === 'ArrowRight') {
            stopAutoPlay();
            nextSlide();
            startAutoPlay();
        }
    });
    
    // Obs≈Çuga dotykowa dla urzƒÖdze≈Ñ mobilnych
    let touchStartX = 0;
    let touchEndX = 0;
    
    heroSlider.addEventListener('touchstart', (e) => {
        touchStartX = e.changedTouches[0].screenX;
    }, { passive: true });
    
    heroSlider.addEventListener('touchend', (e) => {
        touchEndX = e.changedTouches[0].screenX;
        handleSwipe();
    }, { passive: true });
    
    function handleSwipe() {
        const swipeThreshold = 50; // Minimalna odleg≈Ço≈õƒá przesuniƒôcia
        
        if (touchEndX - touchStartX > swipeThreshold) {
            // Przesuniƒôcie w prawo - poprzedni slajd
            stopAutoPlay();
            prevSlide();
            startAutoPlay();
        } else if (touchStartX - touchEndX > swipeThreshold) {
            // Przesuniƒôcie w lewo - nastƒôpny slajd
            stopAutoPlay();
            nextSlide();
            startAutoPlay();
        }
    }
    
    // Zatrzymujemy automatyczne prze≈ÇƒÖczanie przy najechaniu myszkƒÖ
    heroSlider.addEventListener('mouseenter', stopAutoPlay);
    heroSlider.addEventListener('mouseleave', startAutoPlay);
    
    // Zatrzymuj automatyczne prze≈ÇƒÖczanie, gdy strona nie jest widoczna
    document.addEventListener('visibilitychange', () => {
        if (document.hidden) {
            stopAutoPlay();
        } else {
            startAutoPlay();
        }
    });
    
    // Sprawdzamy, czy slajder jest w widoku przy przewijaniu
    window.addEventListener('scroll', () => {
        const rect = heroSlider.getBoundingClientRect();
        const isInView = (
            rect.top >= -rect.height &&
            rect.bottom <= (window.innerHeight || document.documentElement.clientHeight) + rect.height
        );
        
        if (isInView) {
            startAutoPlay();
        } else {
            stopAutoPlay();
        }
    });
    
    // Startujemy automatyczne prze≈ÇƒÖczanie
    startAutoPlay();
    
    // Domy≈õlnie ju≈º aktywowali≈õmy pierwszy slajd, wiƒôc nie musimy tego robiƒá ponownie
    console.log('‚úÖ Slider g≈Ç√≥wny zainicjalizowany poprawnie');
}

